$(document).ready(function(){
    $(window).scroll(function () {
           if ($(this).scrollTop() > 50) {
               $('#go-top-icon').fadeIn();
           } else {
               $('#go-top-icon').fadeOut();
           }
       });
       // scroll body to 0px on click
       $('#go-top-icon').click(function () {
           $('#go-top-icon').tooltip('hide');
           $('body,html').animate({
               scrollTop: 0
           }, 800);
           return false;
       });
       
       $('#go-top-icon').tooltip('show');

});